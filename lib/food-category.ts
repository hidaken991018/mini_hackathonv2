/**
 * 食材名から常備品かどうかを判定する
 *
 * @param name 食材名
 * @returns true = 常備品（調理で減らさない）、false = 使い切り（調理で減らす）
 */
export function isStapleFood(name: string): boolean {
  const lower = name.toLowerCase();
  return STAPLE_KEYWORDS.some((keyword) =>
    lower.includes(keyword.toLowerCase())
  );
}
